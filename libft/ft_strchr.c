#include <stdlib.h>

char	*ft_strchr(const char *s, int c)
{
	while (c > 128)
		c -= 128;
	while (*s)
	{
		if (*s == c)
			return ((char *)s);
		s++;
	}
	if (c == '\0')
		return ((char *)s);
	return (NULL);
}
